# 6. 응답 파싱 함수
# ──────────────────────────────────────────────────────────────
def parse_routine(response_text: str) -> dict:
    """
    Claude 응답 텍스트 → weeklyRoutine 딕셔너리
    마크다운 코드블록 자동 제거 후 파싱
    """
    cleaned = response_text.strip()
    if cleaned.startswith("```"):
        cleaned = "\n".join(cleaned.split("\n")[1:])
    if cleaned.endswith("```"):
        cleaned = "\n".join(cleaned.split("\n")[:-1])
    return json.loads(cleaned)


