const S3_BASE    = 'https://lyftaweb.s3.us-east-2.amazonaws.com/GymvisualMP4';
const THUMB_BASE = 'https://lyfta.app/thumbnails';

export function getLyftaMedia(name) {
  const img = LYFTA_IMG_CODES[name];
  if (!img) return null;
  const vid = img.slice(0, 5) + '2' + img.slice(6);
  return {
    videoUrl: `${S3_BASE}/${vid}.mp4`,
    thumbUrl: `${THUMB_BASE}/${img}.jpg`,
  };
}
